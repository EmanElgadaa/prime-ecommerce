var selectedLang = "";
var rtl = false;

function checkLanguage() {
	selectedLang = getFromLocalStorage('userLanguage', "");
	if (selectedLang === "" || selectedLang === "null") {
		selectedLang = getCookie("userLanguage");
		if (selectedLang === "" || selectedLang === "null")
			selectedLang = $("#languageSelect").val();
	}
}

function updateLanguage() {

	setCookie("userLanguage", selectedLang, 1000);
	saveToLocalStorage("userLanguage", selectedLang);
	$("#languageSelect").val(selectedLang)

	updateText(selectedLang);
	updateInputValues(selectedLang);
}

// Function to update element text based on language
function updateText() {
	$(".lang").each(function (index, ele) {
		try {
			const key = $(this).attr("key");
			if (key && translations[selectedLang][key]) {
				$(this).text(translations[selectedLang][key]);
			} else {
				console.warn(`Missing translation key: ${key} for language: ${selectedLang}`);
			}
		} catch (error) {
			
		}

	});
}

// Function to update input placeholders and button value
function updateInputValues() {
	for (const inputId in translations[selectedLang]) {
		try {
			if (inputId.startsWith("input")) {
				$(`#${inputId}`).attr("placeholder", translations[selectedLang][inputId]);
			} else if (inputId === "input6") {
				$(`#${inputId}`).attr("value", translations[selectedLang][inputId]);
			}
		} catch (error) {
			
		}
		
	}
}

