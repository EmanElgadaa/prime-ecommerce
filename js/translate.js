function updateDocument() {

	updateLanguage();

	$("body").attr("dir", selectedLang === "en" ? "ltr" : "rtl"); // Update direction
	$("body").css("text-align", selectedLang === "ar" ? "right" : "left"); // Optional text alignment
	$(".edit-padding").css("padding-right", selectedLang === "ar" ? "0" : ""); // Optional padding

	const rtlStyle = $("link[href='css/style-rtl.css']") // Existing link for CSS
	if (selectedLang === "ar") {
		if (!rtlStyle.length)  // Check if CSS file is not included
			$("head").append('<link rel="stylesheet" href="css/style-rtl.css">');// Add CSS file if not Arabic
	} else {
		if (rtlStyle.length)
			rtlStyle.remove();// Remove CSS file if not Arabic
	}

	if (typeof DrawDocument !== "undefined")
		DrawDocument();

	updateCarousel();
	updateSwipper();
	updateOwl();
}

