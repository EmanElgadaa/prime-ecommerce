  function getStoredLanguage() {
    const storedLang = localStorage.getItem('userLanguage');
    return storedLang || 'en'; // Default to English if not stored
  }
  
  function setStoredLanguage(lang) {
    localStorage.setItem('userLanguage', lang);
  }
  
  function changeLanguage(language) {
    setStoredLanguage(language); // Update local storage
  
    // Update Content Translations
    const elements = document.querySelectorAll('[key]');
    elements.forEach(element => {
      const key = element.getAttribute('key');
      element.textContent = messages[language][key];
    });
  
    // Update CSS File (using language code)
    const cssFile = `css/style_${language}.css`;
    const link = document.querySelector('link[rel="stylesheet"]');
    if (link) {
      link.href = cssFile;
    }
    const scriptFile = `js/owl_${language}.js`;
    const existingScript = document.querySelector(`script[src="${scriptFile}"]`);

  if (!existingScript) {
    const script = document.createElement('script');
    script.src = scriptFile;
    script.defer = true; // Ensure deferred loading
    document.body.appendChild(script);
  }
  
  }
  
  const storedLanguage = getStoredLanguage(); // Get stored language
  changeLanguage(storedLanguage); // Apply stored language on initial load
  
  // Handle language selection from dropdown
  const languageSelect = document.getElementById('languageSelect');
  languageSelect.addEventListener('change', function() {
    const selectedLang = this.value;
    changeLanguage(selectedLang);
  });
